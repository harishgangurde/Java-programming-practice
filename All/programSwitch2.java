class Switch2{
	public static void main(String[] args){
		char ch = 'C';
		switch(ch){
			case 'A':
				System.out.println("Anger never lead to PEACE");
				break;
			case 'B' :
				System.out.println("Be Kind");
				break;
			case 'C' :
				System.out.println("Life is the IRONY of DREAMS");
				break;
			case 'D' :
				System.out.println("History donesn't talk about LOSERS");
				break;
			default :
				System.out.println("wrong Input");
		}
	}
}
