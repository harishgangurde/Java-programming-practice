class Bitwise{
	public static void main(String[] args){
		int firstInt=5;
		int secondInt=3;
		System.out.println(firstInt & secondInt); 
		System.out.println(firstInt | secondInt); 
		System.out.println(firstInt ^ secondInt); 
		System.out.println(firstInt << secondInt); 
		System.out.println(firstInt >> secondInt);
	}
}	
